# Dictionaries are unordered mappings for string objects, dictionaries use a key-value pairing instead.

my_dict = {"key1": "value1", "key": "value2"}
print(my_dict)

print(my_dict["key1"])


prices_lookup = {"apple": 2.99, "oranges": 1.99, "milk": 5.80}

print(prices_lookup["apple"])

print(prices_lookup.keys())
print(prices_lookup.values())
print(prices_lookup.items())
