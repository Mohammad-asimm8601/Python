# Equal operator ==

print(2 == 1)
print("hello" == "hello")
print("bye" == "Bye")
print("2" == 2)
print(2.0 == 2)


# Not equal to operator !=

print(3 != 2)
print("hello" != "Hello")
print(2.31 != 2.3)


# greater than , less than operator and less than equal to

print(2 > 1)
print(5 < 3)
print(4 <= 1)
